 var num1 = 4;
 var num2 = 5;

 // && (And)
 var resultadoTrue1 = num1 == 4 && num2 > 6;
 console.log ("El resultado es con and: ", resultadoTrue1);

 // || (Or)
 var resultadoTrue1 = num1 == 4 || num2 > 6;
 console.log ("El resultado es con or: ", resultadoTrue1);

 // | (Not)
 var resultadoTrue1 =! num1 == 4 | num2 > 6;
 console.log ("El resultado es con Not: ", resultadoTrue1);

